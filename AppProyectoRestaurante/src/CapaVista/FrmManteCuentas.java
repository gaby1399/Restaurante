    private void jBtnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConsultarActionPerformed
        if(jTblCuenta.getSelectedRow()>= 0){
            String codigo = jTblCuenta.getValueAt(jTblCuenta.getSelectedRow(),0).toString();
            JDlgCuenta dlgCuenta = new JDlgCuenta( Integer.parseInt(codigo));//OJO se envía al contructor del JDialog el código del cuenta
            dlgCuenta.setModal(true);//Indica que no se puede pasar a la ventana anterior mientras no se cierre esta
            dlgCuenta.setLocationRelativeTo(null);
            dlgCuenta.setVisible(true);
        }
        else{
            JOptionPane.showMessageDialog(null,"Debe seleccionar el departamento a consultar");
            return;
        }
        jTblCuenta.clearSelection();
    }//GEN-LAST:event_jBtnConsultarActionPerformed

