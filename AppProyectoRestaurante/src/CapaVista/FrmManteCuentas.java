    private void jBtnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnEliminarActionPerformed
         if(jTblCuenta.getSelectedRow()==-1){
            JOptionPane.showMessageDialog(null,"Debe seleccionar el departamento a eliminar");
            return;
        }

        String codigo = jTblCuenta.getValueAt(jTblCuenta.getSelectedRow(),0).toString();
        try {
            Cuenta.eliminarCuenta(Integer.parseInt(codigo));
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error al eliminar el departamento\n"+ex.toString());
            System.exit(0);
        }
        llenaTablaCuenta(); //actualiza la tabla con el nuevo cuenta
    }//GEN-LAST:event_jBtnEliminarActionPerformed

     private void llenaTablaCuenta() {
        //Llama al método que llena la lista con los Cuenta del ArrayList
        //Llena la tabla con los Cuenta que se grabaron en el ArrayList
        modeloTabla.setRowCount(0);//Limpia la tabla
        Object[] datos = new Object[3];        
        ArrayList<Cuenta> lista= new ArrayList<Cuenta>();
     try {
         lista=Cuenta.listadoCuenta();
     } catch (Exception ex) {
         JOptionPane.showMessageDialog(null, "Error al leer el archivo, la aplicacion se cerrara");
         System.exit(0);
     }
      SimpleDateFormat formatoFecha=new SimpleDateFormat("dd/MM/yy");
         for (int i = 0; i < lista.size(); i++) {
          datos[0] = lista.get(i).getNumCuenta();
          datos[1] = formatoFecha.format(lista.get(i).getFecha());
          datos[2] = lista.get(i).getPago().getMontoPago();
          modeloTabla.addRow(datos);//Agrega el arreglo como una nueva fila de la tabla
        }
    }
    
     
     
