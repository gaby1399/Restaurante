    public void crearCuenta(){
        Usuario usuario1;
        SimpleDateFormat formatoFecha=new SimpleDateFormat("dd/MM/yy");
        SimpleDateFormat formatoHoras=new SimpleDateFormat("hh:mm:ss");
      if(JDlgUsuarios.permiso){
         usuario1=new Usuario(TipoUsuario.Administrador);
      } else{
         usuario1=new Usuario(TipoUsuario.Salonero); 
      }
      cuenta1= new Cuenta(usuario1);
      int mesa=(int) jSpnMesa.getValue();
      cuenta1.setNumMesa(mesa);
      jTxtNumCuenta.setText(String.valueOf(cuenta1.getNumCuenta()));
      jTxtFecha.setText(String.valueOf(formatoFecha.format(cuenta1.getFecha())));
      jTxtHora.setText(String.valueOf(formatoHoras.format(cuenta1.getHora())));
    }
    
          
           
    public void llenarAdicional() throws Exception{
     
         jCbxAdicional.removeAllItems();
        arrayProducto=Producto.listadoProducto();
        for (int i = 0; i < arrayProducto.size(); i++) {
           Producto prod=arrayProducto.get(i);
            if(prod instanceof Adicional){
                jCbxAdicional.addItem((Adicional)prod );
                
            }
            
            
        }
        jCbxAdicional.setSelectedIndex(-1);
        
    }
    
