    private void jBtnAñadirPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnAñadirPedidoActionPerformed
        Object[] tabla =new Object[5];
        if(jRdbPlato.isSelected()){
           
           PlatoPrincipal plato=(PlatoPrincipal) jCbxPlato.getSelectedItem();
            producto= new PlatoPrincipal(plato.getCodigo(), plato.getNombre(),plato.getPrecioBase());
             linea=new LineaDetalle(producto, Integer.parseInt(jTxtCantidad.getText()));
             linea.setIndicaciones(jTextIndicaciones.getText());
        }else{
            if(jRdbRefresco.isSelected()){
                Refresco refresco= (Refresco) jCbxResfresco.getSelectedItem();
                Tamaño tamaño= (jChbGrande.isSelected())?Tamaño.Grande:Tamaño.Pequeño;
                producto=new Refresco(refresco.getCodigo(),tamaño, refresco.getNombre(),refresco.getPrecioBase());
//                producto.montoExtra();
                 linea=new LineaDetalle(producto, Integer.parseInt(jTxtCantidad.getText()));
                  linea.setIndicaciones("Monto extra: " + String.valueOf(producto.montoExtra()));
                  jTxtExtra.setText(String.valueOf(producto.montoExtra()));
            }else{
                if(jRdbAdicional.isSelected()){
                    Adicional adic=(Adicional) jCbxAdicional.getSelectedItem();
                    Tamaño tamaño= (jChbGrande.isSelected())?Tamaño.Grande:Tamaño.Pequeño;
                    producto=new Adicional(adic.getCodigo(), tamaño, adic.getNombre(), adic.getPrecioBase());
//                    producto.montoExtra();
                      linea=new LineaDetalle(producto, Integer.parseInt(jTxtCantidad.getText()));
                     linea.setIndicaciones("Monto extra: " + String.valueOf(producto.montoExtra()));
                      jTxtExtra.setText(String.valueOf(producto.montoExtra()));
             }else{
                     PlatoPrincipal plato=(PlatoPrincipal) jCbxPlato.getSelectedItem();
                     Refresco refresco= (Refresco) jCbxResfresco.getSelectedItem();
                      Adicional adic1=(Adicional) jCbxAdicional.getSelectedItem();
                      Adicional adic2=(Adicional) jCbxAdComb.getSelectedItem();
                     boolean refil=jChbRefil.isSelected();
                     boolean agrandar=jChbAgrandar.isSelected(); 
                     producto= new Combo(String.valueOf(cont++), plato, refresco,refil , agrandar);
                       ((Combo)producto).agregarAdicional(adic1);
                       ((Combo)producto).agregarAdicional(adic2);
                         jTextRefil.setText(String.valueOf(((Combo)producto).cobroRefil()));
                         jTextAgrandar.setText(String.valueOf(((Combo)producto).cobroAgrandado()));
                      linea=new LineaDetalle(producto, Integer.parseInt(jTxtCantidad.getText()));
                      linea.setIndicaciones(jTextIndicaciones.getText());
                      jLbDes.setVisible(true);
                      jTxtDesc.setVisible(true);
                      jTxtDesc.setText(String.valueOf(((Combo)producto).descuento()));
            }
                
            }
        }
        limpiar();
         
             linea.precioLinea();
            cuenta1.agregarProducto(linea);
            tabla[0]=producto.getCodigo();
            tabla[1]=producto.getNombre();
            tabla[2]=producto.montoTotal();
            tabla[3]=linea.getCantidad();
            tabla[4]=linea.getIndicaciones();
            modeloTable.addRow(tabla);
            jTxtaFactura.setText(cuenta1.toString());
            jTxtSubtotal.setText(String.format("%.2f",cuenta1.subTotal())); 
           jTxtImpuesto.setText(String.format("%.2f",cuenta1.impuestos()));
           jTxtServicio.setText(String.format("%.2f",cuenta1.impServicio()));
           jTxtTotal.setText(String.format("%.2f",cuenta1.Total()));   
     
           jBtnAñadirPedido.setEnabled(false);
           jBtnNuevoPedido.setEnabled(true);
           jPanel1.setEnabled(false); 
           jPanel2.setEnabled(false);
    }//GEN-LAST:event_jBtnAñadirPedidoActionPerformed

