    private void jBtnEliminarPedidosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnEliminarPedidosActionPerformed
         if(jTblPedidos.getSelectedRow()==-1){
            JOptionPane.showMessageDialog(null,"Debe seleccionar el departamento a eliminar");
            return;
        }
         int prod=jTblPedidos.getSelectedRow();
         ArrayList listaEliminar=new ArrayList();
           for (int i = 0; i < cuenta1.getArrayDetalle().size(); i++) {
               listaEliminar.add(cuenta1.getArrayDetalle().get(i));
          }
               cuenta1.getArrayDetalle().clear();
           for (int i = 0; i < listaEliminar.size(); i++) {
            if(i!=prod){
               cuenta1.agregarProducto((LineaDetalle) listaEliminar.get(i));
            }
        }
         modeloTable.removeRow(jTblPedidos.getSelectedRow());
          jTxtaFactura.setText(cuenta1.toString());
          jTxtSubtotal.setText( String.format("%.2f",cuenta1.subTotal())); 
          jTxtImpuesto.setText( String.format("%.2f",cuenta1.impuestos()));
          jTxtServicio.setText( String.format("%.2f",cuenta1.impServicio()));
          jTxtTotal.setText(String.format("%.2f",cuenta1.Total()));   
    }//GEN-LAST:event_jBtnEliminarPedidosActionPerformed

