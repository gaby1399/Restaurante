    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
      String codigo=jTxtCodigo.getText();
       
      String nombre= jTxtNombre.getText();
      double precio=Double.parseDouble(jTxtPrecio.getText());
     if(FrmProducto.plato){
          FrmProducto.setProductoMantenimento(new PlatoPrincipal(codigo, nombre, precio));
     }else{
         if(FrmProducto.adic){
              FrmProducto.setProductoMantenimento(new Adicional(codigo, nombre, precio));
         
         }else{
             FrmProducto.setProductoMantenimento(new Refresco(codigo, nombre, precio)); 
            }
     }
     this.dispose();
    }//GEN-LAST:event_btnAceptarActionPerformed

