    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened

        switch(mantenimento){
            case AGREGAR:
              jTxtCodigo.setEditable(true);
              jTxtNombre.setEditable(true);
              jTxtPrecio.setEditable(true);
                setTitle("Nuevo Producto");
              break;
            case MODIFICAR:
              jTxtCodigo.setEditable(false);
              jTxtNombre.setEditable(true);
              jTxtPrecio.setEditable(true);
              jTxtNombre.selectAll();
                setTitle("Modificar Producto");  
               break; 
            case CONSULTAR:
              jTxtCodigo.setEditable(false);
              jTxtNombre.setEditable(false);
              jTxtPrecio.setEditable(false);
              btnAceptar.setEnabled(false);
                setTitle("Consultar Producto");  
               break; 
        }
        jTxtCodigo.setText(codigo);
        jTxtNombre.setText("");
        jTxtPrecio.setText("");
        try{
          productoBuscar=Producto.consultarProducto(codigo);  
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Error en la busqueda"+
            e.toString()+"\n"+e.getMessage());
        }
        if(productoBuscar!=null){
            jTxtNombre.setText(productoBuscar.getNombre());
            jTxtPrecio.setText(String.valueOf(productoBuscar.getPrecioBase()));
        }
    }//GEN-LAST:event_formWindowOpened

