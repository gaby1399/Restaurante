    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened

                setTitle("Consulta de una Cuenta");
                
        
        jTxtNumC.setText(String.valueOf(codigoCuenta));
       
        try {
           cuentaBuscar = Cuenta.consultarCuenta(codigoCuenta);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al buscar la cuenta\n"+
                e.toString() + "\n" + e.getMessage());
        }  
        if(cuentaBuscar != null){
            jTxtaCuentaR.setText(cuentaBuscar.toString());
}
    }//GEN-LAST:event_formWindowOpened

